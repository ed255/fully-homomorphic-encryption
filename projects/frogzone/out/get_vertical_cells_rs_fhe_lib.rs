fn prune<'a, E: BoolEvaluator>(
    temp_nodes: &mut HashMap<usize, FheBool<'a, E>>,
    temp_node_ids: &[usize],
) {
  for x in temp_node_ids {
    temp_nodes.remove(&x);
  }
}

